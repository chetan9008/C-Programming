#include<stdio.h>
int main()
{
    int a[6][6]={1,2,3,4,5,6,
    1,2,3,4,5,6,
    1,2,3,4,5,6,
    1,2,3,4,5,6,
    1,2,3,4,5,6,
    1,2,3,4,5,6
    };
    int b[6][6]={1,2,3,4,5,6,
    1,2,3,4,5,6,
    1,2,3,4,5,6,
    1,2,3,4,5,6,
    1,2,3,4,5,6,
    1,2,3,4,5,6
    };
    int i,j,k,mul[6][6];
    for(i=0;i<6;i++)
    {
        for(j=0;j<6;j++)
        {
mul[i][j]=0;
            for(k=0;k<6;k++)
            {
                mul[i][j]+=a[i][k]*b[k][j];
            }
        }
    }
    for(i=0;i<6;i++)
    {
        for(j=0;j<6;j++)
        {
              printf("%d\t",mul[i][j]);
        }
        printf("\n");
    }
}